    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        // TODO add your handling code here:
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String nota1=txtNota1.getText();
        String nota2=txtNota2.getText();
        String nota3=txtNota3.getText();
        double promedio=(Double.parseDouble(nota1)+Double.parseDouble(nota2)+Double.parseDouble(nota3))/3;
        if(!nombre.equals("")&&!apellido.equals("")&&!nota1.equals("")&&!nota2.equals("")&&!nota3.equals("")){
           int con=JOptionPane.showConfirmDialog(rootPane, "Â¿Estas seguro de ingresar estos datos?", "Confirmacion", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
           if(con==0){
             DatosEstudiante datos= new DatosEstudiante(nombre,apellido,Double.parseDouble(nota1),Double.parseDouble(nota2),Double.parseDouble(nota3),promedio);  
              dato.add(datos); 
               txtNombre.setText("");
               txtApellido.setText("");
               txtNota1.setText("");
               txtNota2.setText("");
               txtNota3.setText("");
           }else{
               txtNombre.setText("");
               txtApellido.setText("");
               txtNota1.setText("");
               txtNota2.setText("");
               txtNota3.setText("");
           }
        }else{
          JOptionPane.showMessageDialog(rootPane, "Debe ingresar todos los campos");  
        }
        
        
        
    }//GEN-LAST:event_ingresarActionPerformed

    private void mostrarPromediosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarPromediosActionPerformed
        // TODO add your handling code here:
        
        txtDatos.setText("");
        for(DatosEstudiante datos:dato){
            if(txtDatos.getText().equals("")){
                txtDatos.setText(datos.getNombre()+"        "+datos.getApellido()+"        "+datos.getNota1()+"        "+datos.getNota2()+"        "+datos.getNota3()+"        "+datos.getPromedio());
            }else{
                txtDatos.setText(txtDatos.getText()+"\n"+datos.getNombre()+"        "+datos.getApellido()+"        "+datos.getNota1()+"        "+datos.getNota2()+"        "+datos.getNota3()+"        "+datos.getPromedio());
            }
        }
        
    }//GEN-LAST:event_mostrarPromediosActionPerformed

    private void reiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reiniciarActionPerformed
        // TODO add your handling code here:
        txtNombre.setText("");
               txtApellido.setText("");
               txtNota1.setText("");
               txtNota2.setText("");
               txtNota3.setText("");
               dato.removeAll(dato);
                   txtDatos.setText("");
        
    }//GEN-LAST:event_reiniciarActionPerformed

